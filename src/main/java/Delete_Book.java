    private void delete_book_button_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_book_button_ActionPerformed
        // TODO add your handling code here:
        if(found==0) 
            JOptionPane.showInternalConfirmDialog(null, "PLEASE ENTER VALID BOOK ID ", "DELETE BOOK",JOptionPane.PLAIN_MESSAGE);
        else if(!book_location.equals("LIBRARY"))
            JOptionPane.showInternalConfirmDialog(null, "BOOK CAN'T BE DELETED AS IT IS NOT AVAILABLE IN THE LIBRARY RIGHT NOW ", "DELETE BOOK",JOptionPane.PLAIN_MESSAGE);

        else {
            try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            st.executeUpdate("delete from books where BOOK_ID='"+book_id+"'");
            int yesOrNo = JOptionPane.showInternalConfirmDialog(null, "DO YOU REALLY WANT TO DELETE THIS BOOK", "DELETE BOOK", JOptionPane.YES_NO_OPTION);
            if(yesOrNo==0) {
                JOptionPane.showInternalConfirmDialog(null, "BOOK DATA DELETED SUCCESSFULLY", "DELETE BOOK",JOptionPane.PLAIN_MESSAGE);
                setVisible(false);
                new Delete_Book().setVisible(true);
            }
                
            } catch(Exception err) {
            JOptionPane.showInternalMessageDialog(null, err);
            }
            
        }   

    }//GEN-LAST:event_delete_book_button_ActionPerformed

    
    
