    private void search_book_id_button_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_book_id_button_ActionPerformed
        // TODO add your handling code here:
         book_id  = book_id_field_.getText();
           try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ResultSet res = st.executeQuery("Select * from books where book_id='"+book_id+"'");
            //book_table_.setModel(DbUtils.resultSetToTableModel(res));
                if(!res.first()) {
                JOptionPane.showInternalConfirmDialog(null, "BOOK ID NOT FOUND ", "SEARCH BOOK",JOptionPane.PLAIN_MESSAGE);
                    setVisible(false);
                    new Delete_Book().setVisible(true);
                } else {
                book_name_field_.setText(res.getString(3));
                book_location_field_.setText(res.getString(8));
                book_location = book_location_field_.getText();
                found = 1;
                }
            } catch(Exception err) {
            JOptionPane.showInternalMessageDialog(null, err);
            }
    }//GEN-LAST:event_search_book_id_button_ActionPerformed
                                                 

