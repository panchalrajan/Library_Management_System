    private void search_student_button_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_student_button_ActionPerformed
        // TODO add your handling code here:
                roll_no  = student_roll_no_field_.getText();
           try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ResultSet res = st.executeQuery("Select * from students where roll_no='"+roll_no+"'");
                if(!res.first()) {
                JOptionPane.showInternalConfirmDialog(null, "STUDENT RECORD NOT FOUND ", "SEARCH STUDENT",JOptionPane.PLAIN_MESSAGE);
                } else {
                student_name_field_.setText(res.getString(2));
                found_student = 1;
                }
            } catch(Exception err) {
            JOptionPane.showInternalMessageDialog(null, err);
            }
    }//GEN-LAST:event_search_student_button_ActionPerformed

    String book_id = " ";
    private void search_book_button_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_book_button_ActionPerformed
        // TODO add your handling code here:
        if(found_student == 0) {
            JOptionPane.showInternalConfirmDialog(null, "PLEASE ENTER STUDENT ROLL NO FIRST ", "SEARCH BOOK",JOptionPane.PLAIN_MESSAGE);

        }
        else {
               book_id  = book_id_field_.getText();
           try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ResultSet res = st.executeQuery("Select * from books where book_id='"+book_id+"'");
            //book_table_.setModel(DbUtils.resultSetToTableModel(res));
                if(!res.first()) {
                JOptionPane.showInternalConfirmDialog(null, "BOOK ID NOT FOUND ", "SEARCH BOOK",JOptionPane.PLAIN_MESSAGE);
                } else {
                book_title_field_.setText(res.getString(3));
                book_location_field_.setText(res.getString(8));
                ResultSet issueDate = st.executeQuery("Select CURDATE();");
                issueDate.next();
                issue_date_field_.setText(issueDate.getString(1));
                found_book = 1;
                book_location = book_location_field_.getText();
                }
            } catch(Exception err) {
            JOptionPane.showInternalMessageDialog(null, err);
            }
                
                }

    }//GEN-LAST:event_search_book_button_ActionPerformed

