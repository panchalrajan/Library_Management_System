    private void issue_book_button_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issue_book_button_ActionPerformed
        //Add Book and show conformation
        if(found_student==0)
            JOptionPane.showInternalConfirmDialog(null, "PLEASE ENTER A VALID STUDENT ROLL FIRST ", "SEARCH BOOK",JOptionPane.PLAIN_MESSAGE);
        else if(found_book==0)
            JOptionPane.showInternalConfirmDialog(null, "PLEASE ENTER A VALID BOOK ID FIRST ", "SEARCH BOOK",JOptionPane.PLAIN_MESSAGE);
        else if(!book_location.equals("LIBRARY")) {
            JOptionPane.showInternalConfirmDialog(null, "BOOK IS NOT AVAILABLE IN LIBRARY! CAN'T BE ISSUED ", "ISSUE BOOK",JOptionPane.PLAIN_MESSAGE);
        }
        else {
            String student_name = student_name_field_.getText();
            String book_title = book_title_field_.getText();
            String issued_date = issue_date_field_.getText();
            String issue_duration = (String)issue_duration_field_.getSelectedItem();

            
            try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet dueDate = st.executeQuery("select DATE_ADD(CURDATE(), INTERVAL '"+issue_duration+"' DAY);");
                dueDate.next();
                due_date = dueDate.getString(1);
            st.executeUpdate("insert into issued(ROLL_NO, STUDENT_NAME, BOOK_ID, BOOK_TITLE, ISSUED_DATE, DUE_DATE) values('"+roll_no+"' , '"+student_name+"', '"+book_id+"', '"+book_title+"', '"+issued_date+"','"+due_date+"')");
            st.executeUpdate("UPDATE BOOKS SET BOOK_LOCATION = '"+student_name+"' WHERE BOOK_ID = '"+book_id+"' ");
            JOptionPane.showInternalConfirmDialog(null, "BOOK ISSUED SUCCESSFULLY", "ISSUE BOOK",JOptionPane.PLAIN_MESSAGE);
            setVisible(false);
            new Issue_Book().setVisible(true);
            } catch(Exception err) {
            JOptionPane.showInternalMessageDialog(null, err);
            }
        }
            
    }//GEN-LAST:event_issue_book_button_ActionPerformed

