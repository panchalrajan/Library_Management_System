    private void search_details_button_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_details_button_ActionPerformed
        // TODO add your handling code here:
                 book_id  = book_id_field_.getText();
           try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ResultSet res = st.executeQuery("Select * from issued where book_id='"+book_id+"' AND STATUS = 'ISSUED'");
            //book_table_.setModel(DbUtils.resultSetToTableModel(res));
                if(!res.first()) {
                JOptionPane.showInternalConfirmDialog(null, "BOOK NOT ISSUED TO ANY STUDENT", "SEARCH BOOK",JOptionPane.PLAIN_MESSAGE);
                    setVisible(false);
                    new Returned_Book().setVisible(true);
                } else {
                book_titile_field_.setText(res.getString(5));
                roll_no_field_.setText(res.getString(2));
                student_name_field_.setText(res.getString(3));
                due_date_field_.setText(res.getString(7)); 
                String due_date = (String)res.getString(7);
                
                ResultSet returnDate = st.executeQuery("Select CURDATE();");
                returnDate.next();
                return_date = (String)returnDate.getString(1);
                
                ResultSet fineOnBook = st.executeQuery("select datediff('"+return_date+"', '"+due_date+"');");
                fineOnBook.next();
                String finalFine = (String)fineOnBook.getString(1);
                if(fineOnBook.getInt(1)<0) {
                    finalFine = "0";  
                } 
                fine_on_book_field_.setText(finalFine);
                found = 1;
                }
            } catch(Exception err) {
            JOptionPane.showInternalMessageDialog(null, err);
            }
    }//GEN-LAST:event_search_details_button_ActionPerformed

