    private void return_book_button_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_return_book_button_ActionPerformed
        //Add Book and show conformation
        if(found==0)
        JOptionPane.showInternalConfirmDialog(null, "PLEASE ENTER BOOK ID FIRST", "RETURN BOOK",JOptionPane.PLAIN_MESSAGE);
        else {
            roll_no = roll_no_field_.getText();
            finalFine = fine_on_book_field_.getText();
               
            try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("UPDATE ISSUED SET RETURNED_DATE = '"+return_date+"',STATUS ='RETURNED' WHERE BOOK_ID = '"+book_id+"' ");
            st.executeUpdate("UPDATE BOOKS SET BOOK_LOCATION ='LIBRARY' WHERE BOOK_ID = '"+book_id+"' ");
            st.executeUpdate("UPDATE STUDENTS SET FINE =FINE + '"+finalFine+"'  WHERE ROLL_No = '"+roll_no+"' ");
            JOptionPane.showInternalConfirmDialog(null, "BOOK RETURNED SUCCESSFULLY", "RETURN BOOK",JOptionPane.PLAIN_MESSAGE);
            setVisible(false);
            new Returned_Book().setVisible(true);
            } catch(Exception err) {
            JOptionPane.showInternalMessageDialog(null, err);
            }
        }
    }//GEN-LAST:event_return_book_button_ActionPerformed

